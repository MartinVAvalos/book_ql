        <section id="architecture" class="block">
          <h2>Architecture</h2>
          <p>
            The Cerritos Library’s architecture blends traditional and futuristic elements. As the first floor of the Library offers
            traditional library services, the architectural design is also traditional. The first floor design features concrete framing the
            structure in the Pavilion architectural style that was used in the earlier phases of the Library. As the second and third floors
            of the Library are future-oriented, the exterior is wrapped in a golden skin of titanium. The Library is the first titanium-clad
            public building in North America. Titanium expresses the concept of change as it has subtle color shifts from reflecting the
            angle of the sun and atmospheric conditions. Titanium also allows for a fluid architectural design with compound curves. The
            material suggests the Library's "Save the Planet" theme as it does not have a negative impact on the environment and is
            maintenance free. The building was designed by Charles Walton and Associates.
          </p>
